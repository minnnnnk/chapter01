package com.javaex.ex04;

public class Ex06 {

	public static void main(String[] args) {
		
			//y축 세로 반복
			for (int i= 1 ; i<=6; i++) {
				
				//x축 가로 반복
				for (int j = 1; j<=6; j++) {
					
					System.out.print("*");
				}
				// 가로 반복 끝나면 줄바꿈
				System.out.println();
			
			}
		
	}

}
